   <h1>Linguagem de Programação PHP</h1>
   
   <h2>O que é PHP?</h2>
   
   <ul>
      <li>PHP significa Hypertext Preprocessor.</li>
      <li>PHP é uma linguagem interpretada, ou seja, não há necessidade de compilação.</li>
      <li>PHP é uma linguagem de script de código aberto amplamente utilizada</li>
      <li>Os scripts PHP são executados no servidor</li>
      <li>PHP pode ser incorporado em HTML.</li>
      <li>PHP é gratuito para baixar e usar</li>
      <li>PHP é uma linguagem orientada a objetos.</li>
      <li>PHP é uma linguagem simples e fácil de aprender.</li>
   </ul>
   <hr>

   <h2>PHP é uma linguagem incrível e popular!</h2>

   <p>É poderoso o suficiente para estar no centro do maior sistema de blogs na web (WordPress)!</p>
   <p>É profundo o suficiente para executar grandes redes sociais!</p>
   <hr>

   <h2>Por que usar PHP</h2>

   <p>PHP é uma linguagem de script do lado do servidor, que é usada para projetar os aplicativos web dinâmicos com o banco de dados MySQL.</p>

   <ul>
      <li>Ele lida com conteúdo dinâmico, banco de dados, bem como rastreamento de sessão para o site.</li>
      <li>Você pode criar sessões em PHP.</li>
      <li>Ele pode acessar cookies variáveis e também definir cookies.</li>
      <li>Isso ajuda a criptografar os dados e aplicar a validação.</li>
      <li>PHP suporta vários protocolos, como HTTP, POP3, SNMP, LDAP, IMAP, e muitos mais.</li>
      <li>Usando a linguagem PHP, você pode controlar o usuário para acessar algumas páginas do seu site.</li>
      <li>Como o PHP é fácil de instalar e configurar, esta é a principal razão pela qual o PHP é a melhor linguagem para aprender.</li>
      <li>O PHP pode manipular os formulários, como - coletar os dados dos usuários usando formulários, salvá-los no banco de dados e retornar informações úteis ao usuário.</li>
      <li>Por exemplo - Formulário de inscrição.</li>
   </ul>
   <hr>

   <h2>O que é um arquivo PHP?</h2>

   <p>Os arquivos PHP podem conter texto, HTML, CSS, JavaScript e código PHP</p>

   <p>O código PHP é executado no servidor e o resultado é retornado ao navegador como HTML simples
   Arquivos PHP têm extensão "<b>.php</b>"</p>
   <hr>

   <h2>O que o PHP pode fazer?</h2>
   <ul>
      <li>PHP pode gerar conteúdo de página dinâmica</li>
      <li>PHP pode criar, abrir, ler, escrever, excluir e fechar arquivos no servidor</li>
      <li>PHP pode coletar dados de formulário</li>
      <li>PHP pode enviar e receber cookies</li>
      <li>PHP pode adicionar, excluir, modificar dados em seu banco de dados</li>
      <li>PHP pode ser usado para controlar o acesso do usuário</li>
      <li>PHP pode criptografar dados</li>
   </ul>

   <p>Com o PHP você não está limitado a saída HTML.
   Você pode produzir imagens ou PDF limas. Você também pode produzir qualquer texto, como XHTML e .XML.</p>
   <hr>

   <h2>Por que PHP?</h2>
   <ul>
      <li>PHP roda em várias plataformas (Windows, Linux, Unix, Mac OS X, etc.)</li>
      <li>PHP é compatível com quase todos os servidores usados hoje (Apache, IIS, etc.)</li>
      <li>PHP suporta uma ampla gama de bancos de dados</li>
      <li>PHP é gratuito. Faça o download a partir do recurso oficial do PHP: www.php.net</li>
      <li>PHP é fácil de aprender e é executado de forma eficiente no lado do servidor</li>
   </ul>
   <hr>

